"""generate_apc.py — emit FacilityHeadSurvey.ent.apc.

Phase 1 minimal: skip-logic and validations are deferred to Phase 2.
The only PROC code is a level postproc that auto-syncs the case to
CSWeb after End Case (so the user doesn't need a separate menu choice).
"""
from pathlib import Path

HERE = Path(__file__).resolve().parent

APC = r'''{ Application 'FacilityHeadSurvey' logic file generated by CSPro }
{ Phase 1: minimal — auto-sync on case completion. Skip-logic + validations
  + GPS + photo + PSGC cascade all deferred to Phase 2 spec-driven build. }

PROC GLOBAL

config string csweb_url;             { from .ent userSettings, spliced by build_all }


function send_to_server()
   if syncconnect(CSWeb, csweb_url) then
      syncdata(PUT, FACILITYHEADSURVEY_DICT);
      syncdisconnect();
   else
      errmsg("Could not reach the sync server. Case saved locally; will retry next sync.");
   endif;
end;


PROC FACILITYHEADSURVEY_LEVEL
postproc
   send_to_server();
'''


def main():
    (HERE / "FacilityHeadSurvey.ent.apc").write_text(APC, encoding="utf-8")
    print(f"wrote FacilityHeadSurvey.ent.apc ({len(APC)} chars; auto-sync on case end)")


if __name__ == "__main__":
    main()
